a = 5
b = 3

#penjumlahan(+)
hasil = a + b
print('Hasil Penjumlahan :',hasil)

#pengurangan(-)
hasil = a - b
print('Hasil Pengurangan :',hasil)

#perkalian(*)
hasil = a * b
print('Hasil perkalian :',hasil)

#pangkat(**)
hasil = a ** b
print('Hasil pangkat :',hasil)

#pembagian(/)
hasil = a / b
print('Hasil pembagian :',hasil)

#pembagian kebawah(//)
hasil = a // b
print('Hasil pembagian kebawah :',hasil)

#sisa bagi(%)
hasil = a % b
print('Hasil sisa bagi :',hasil)

#precedence
hasil = 1 + 2 * 4 - 3
print('Hasil precedence :',hasil)

#precedence
hasil = (1 + 2) * 4 - 3
print('Hasil precedence :',hasil)